
let velocity = 110

switch(velocity * 2) {
    case 100:
        document.write('Sua velocidade é 100 km/s')
        break
    case 160:
        document.write('Sua velocidade é 160 Km/s')
        break
    case 220:
        document.write('Sua velocidade é 220 Km/s')
        break
    default:
        document.write('Velocidade não encontrada')
}